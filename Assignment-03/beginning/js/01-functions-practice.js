function findModulus() {
    "use strict";
    var a, b, modulus;
    a = window.prompt("Enter a number");
    b = window.prompt("Enter another number");
    modulus = (a % b);
    window.document.write(modulus + " is the modulus of " + a + " and  " + b + ".");
}
